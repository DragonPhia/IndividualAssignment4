    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        id = jTextField1.getText();
        firstName = jTextField2.getText();
        lastName = jTextField3.getText();
        email = jTextField4.getText();

        ArrayList temp = new ArrayList<>();

        try (FileReader fileReader = new FileReader("data.txt")) {

            Scanner reader = new Scanner(fileReader);
            String line;
            String[] arr;

            while ((line = reader.nextLine()) != null) {

                //Makes sure ID matches with selected line.
                arr = line.split(",");

                if (arr[0].equals(id)) {
                    temp.add(id + "," + firstName + "," + lastName + "," + email);
                    JOptionPane.showMessageDialog(null, "submitted");
                } else {
                    temp.add(line);
                }
            }

            fileReader.close();

        } catch (Exception e) {
        }

        try (PrintWriter pr = new PrintWriter("data.txt")) {

            for (Object stringArr : temp) {
                pr.println(stringArr);
            }

            pr.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }

        tableLoad();
        reset();
    }//GEN-LAST:event_jButton2ActionPerformed

