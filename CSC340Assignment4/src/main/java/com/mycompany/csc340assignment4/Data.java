    /**
     * Loads/updates table based on data.txt file after every function.
     */
    private void tableLoad() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String filePath = "data.txt";
        File file = new File(filePath);

        try {

            BufferedReader bufferReader = new BufferedReader(new FileReader(file));
            String fileLine = bufferReader.readLine().trim();
            String[] column = fileLine.split(",");
            model.setColumnIdentifiers(column);
            Object[] tableLines = bufferReader.lines().toArray();

            //gets lines from text file and puts them into table.
            for (int i = 0; i < tableLines.length; i++) {
                String line = tableLines[i].toString().trim();
                String[] datarow = line.split(",");
                model.addRow(datarow);

            }

            bufferReader.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }
    }

    /**
     * Delete file line from data.txt.
     *
     * @param file
     * @param deleteLine
     */
    public static void delete(String file, int deleteLine) {

        //Transfers line to a temporary file to be flushed.
        String tempFile = "temp.txt";
        File oldFile = new File(file);
        File newFile = new File(tempFile);

        int line = 0;
        String currentLine;

        try {
            FileWriter fileWriter = new FileWriter(tempFile, true);
            BufferedWriter bufferWriter = new BufferedWriter(fileWriter);
            PrintWriter printWriter = new PrintWriter(bufferWriter);

            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);

            //Prevents deletion of header
            while ((currentLine = br.readLine()) != null) {
                line++;

                if (deleteLine != line) {
                    printWriter.println(currentLine);
                }
            }

            //deletes line
            printWriter.flush();
            printWriter.close();
            fr.close();
            br.close();
            bufferWriter.close();
            fileWriter.close();

            oldFile.delete();

            File delete = new File(file);
            newFile.renameTo(delete);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }

    }

